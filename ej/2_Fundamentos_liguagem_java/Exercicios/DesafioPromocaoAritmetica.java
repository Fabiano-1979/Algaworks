public class DesafioPromocaoAritmetica {
    public static void main(String[] args) {
        int idade1 = 30;
        int idade2 = 40;
        int idade3 = 60;
    
        Float idadeMedia = (idade1 + idade2 + idade3) / 3f; // Aqui, o resultado , foi alcansado simplimente  
        // trocando o tipo da variável idadeMedia e, ao dividir por 3, acrescentar um "f" no número 3
    
        System.out.println("Idade média: " + idadeMedia);
      }
}
